struct custom_hash {
    static uint64_t splitmix64(uint64_t x) {
        // http://xorshift.di.unimi.it/splitmix64.c
        x += 0x9e3779b97f4a7c15;
        x = (x ^ (x >> 30)) * 0xbf58476d1ce4e5b9;
        x = (x ^ (x >> 27)) * 0x94d049bb133111eb;
        return x ^ (x >> 31);
    }

    size_t operator()(uint64_t x) const {
        static const uint64_t FIXED_RANDOM = chrono::steady_clock::now().time_since_epoch().count();
        return splitmix64(x + FIXED_RANDOM);
    }
};

int main() {
		ll n;
		scanf("%I64d", &n);
		vl a(n), b(n);
		map<int, int> freq, a_freq, b_freq;
		F0R(i, n) {
				cin >> a[i] >> b[i];
				freq[a[i]]++;
				if (a[i] != b[i]) freq[b[i]]++;
				a_freq[a[i]]++;
				b_freq[b[i]]++;
		}
		vl candidates;

		for (const auto& pp : freq) {
				if (pp.s >= (n + 1)/2) {
//						cout << pp.f << " is a candidate" << endl;
						candidates.pb(pp.first);
				}
		}
		ll best = INF;
		for (const auto& val : candidates) {
				ll cost = MAX(0, (n + 1)/2 - a_freq[val]);
				best = MIN(best, cost);
		}
		if (best == INF) cout << -1 << endl;
		else cout << best << endl;
//		cout << best << endl;
}
